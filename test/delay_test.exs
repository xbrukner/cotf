  test "Junction delay" do
    m = RoadMap.new("sample_map.txt")
    g = %Global{map: m, tf_duration: 60}
    cars = %{0 => 30, 1 => 4, 3 => 20}

#Check if needed
    assert Delay.junction(g, "A", "B", cars) == %{0 => 41.83281572999747, 1 => 8.035714285714285,
